def cmd_review_pr(args):
    """Review a GitHub Pull Request."""
    from code_review.git_integration import GitHubIntegration
    import tempfile
    
    print("=" * 60)
    print(f"  Review Pull Request #{args.pr_number}")
    print(f"  Repository: {args.repo}")
    print("=" * 60)
    
    try:
        gh = GitHubIntegration()
        
        # Get PR info
        pr_info = gh.get_pr_info(args.repo, args.pr_number)
        print(f"\n  Title: {pr_info['title']}")
        print(f"  Author: {pr_info['author']}")
        print(f"  Branch: {pr_info['head_branch']} → {pr_info['base_branch']}")
        print(f"  Files: {pr_info['files_count']} (+{pr_info['additions']}/-{pr_info['deletions']})")
        
        # Get changed files
        files = gh.get_pr_files(args.repo, args.pr_number)
        python_files = [f for f in files if f.path.endswith('.py')]
        
        if not python_files:
            print("\n✓ No Python files in this PR")
            return 0
        
        print(f"\nFound {len(python_files)} Python file(s):")
        for f in python_files:
            print(f"  [{f.status}] {f.path}")
        
        # Setup output
        output_dir = Path(args.output_dir)
        output_dir.mkdir(parents=True, exist_ok=True)
        
        # Load RAG
        rag_retriever = None
        try:
            from rag_chatbot.chatbot import RAGChatbot
            if Path("./chroma_db").exists():
                print("\nLoading RAG knowledge base...")
                chatbot = RAGChatbot(
                    data_directory="./Data/python",
                    persist_directory="./chroma_db",
                    collection_name="python_best_practices"
                )
                rag_retriever = chatbot.retriever
        except Exception:
            pass
        
        config = AgentConfig(
            output_dir=str(output_dir),
            use_reflection=not args.no_reflection,
            validate_fixes=True
        )
        agent = CodeReviewAgent(config=config, rag_retriever=rag_retriever)
        
        # Clear previous output before starting
        agent.tools.clear_output()
        print("✓ Cleared previous output files")
        
        # Review each file
        all_results = []
        for i, f in enumerate(python_files):
            if f.status != 'D':
                print(f"\n--- Reviewing: {f.path} ---")
                
                # Get file content from PR
                content = gh.get_file_content_from_pr(args.repo, args.pr_number, f.path)
                
                if content:
                    # Save to temp file for review
                    with tempfile.NamedTemporaryFile(mode='w', suffix='.py', delete=False) as tmp:
                        tmp.write(content)
                        tmp_path = tmp.name
                    
                    # Append to report for all files after the first
                    result = agent.review_file(tmp_path, append_report=(i > 0))
                    all_results.append(result)
                    
                    # Cleanup
                    Path(tmp_path).unlink()
        
        # Generate final summary
        _generate_final_summary(agent, all_results, str(output_dir))
        
        # Generate summary
        report_path = output_dir / "codereview.md"
        
        print("\n" + "=" * 60)
        print("  PR Review Complete!")
        print("=" * 60)
        print(f"  Files reviewed: {len(all_results)}")
        print(f"  Report: {report_path}")
        print(f"  PR URL: {pr_info['url']}")
        
        # Post comment to PR if requested
        if args.post_comment and report_path.exists():
            print("\nPosting review comment to PR...")
            report_content = report_path.read_text()
            # Truncate if too long
            if len(report_content) > 60000:
                report_content = report_content[:60000] + "\n\n... (truncated)"
            
            comment = f"## 🤖 AI Code Review\n\n{report_content}"
            if gh.post_pr_comment(args.repo, args.pr_number, comment):
                print("✓ Review posted to PR")
            else:
                print("✗ Failed to post comment")
        
        return 0
        
    except Exception as e:
        print(f"Error: {e}")
        return 1


