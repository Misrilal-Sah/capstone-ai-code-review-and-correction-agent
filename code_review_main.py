def _generate_final_summary(agent, results, output_dir):
    """Append a final summary table to codereview.md."""
    if not results:
        return
    
    output_path = Path(output_dir) / "codereview.md"
    if not output_path.exists():
        return
    
    # Calculate totals
    total_issues = sum(len(r.issues) for r in results)
    avg_confidence = sum(r.confidence for r in results) / len(results)
    corrected_count = sum(1 for r in results if r.corrected_code)
    
    # Build summary table
    summary = f"""

---

# 📊 Full Review Summary

**Command Completed:** {len(results)} files reviewed  
**Generated:** {results[0].timestamp if results else 'N/A'}

## Statistics

| Metric | Value |
|--------|-------|
| **Total Files** | {len(results)} |
| **Total Issues** | {total_issues} |
| **Corrected Files** | {corrected_count} |
| **Average Confidence** | {avg_confidence:.1%} |

## Files Reviewed

| File | Issues | Confidence | Corrected |
|------|--------|------------|-----------|
"""
    
    for r in results:
        filename = Path(r.file_path).name
        corrected = "✅" if r.corrected_code else "❌"
        summary += f"| `{filename}` | {len(r.issues)} | {r.confidence:.0%} | {corrected} |\n"
    
    summary += """
---

*Generated by AI Code Review Agent*
"""
    
    # Append to existing file
    existing = output_path.read_text(encoding='utf-8')
    output_path.write_text(existing + summary, encoding='utf-8')


