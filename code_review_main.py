def cmd_review(args):
    """Handle review command."""
    path = Path(args.path)
    
    if not path.exists():
        print(f"Error: Path not found: {args.path}")
        return 1
    
    if args.dry_run:
        print(f"[DRY RUN] Would review: {path}")
        print(f"[DRY RUN] Output directory: {args.output_dir}")
        print(f"[DRY RUN] Reflection: {'disabled' if args.no_reflection else 'enabled'}")
        print(f"[DRY RUN] Validation: {'disabled' if args.no_validation else 'enabled'}")
        print(f"[DRY RUN] Complexity: {args.complexity}")
        return 0
    
    # Create agent
    config = AgentConfig(
        output_dir=args.output_dir,
        use_reflection=not args.no_reflection,
        validate_fixes=not args.no_validation,
        default_complexity=args.complexity
    )
    
    # Try to load RAG retriever if available
    rag_retriever = None
    try:
        from rag_chatbot.chatbot import RAGChatbot
        from rag_chatbot.retriever import Retriever
        
        # Check if vector database exists
        if Path("./chroma_db").exists():
            print("Loading RAG knowledge base...")
            chatbot = RAGChatbot(
                data_directory="./Data/python",
                persist_directory="./chroma_db",
                collection_name="python_best_practices"
            )
            rag_retriever = chatbot.retriever
            print("✓ RAG knowledge base loaded")
    except Exception as e:
        print(f"Note: RAG not available ({e}). Using default best practices.")
    
    agent = CodeReviewAgent(config=config, rag_retriever=rag_retriever)
    
    # Clear previous output before starting new review
    agent.tools.clear_output()
    print("✓ Cleared previous output files")
    
    # Review
    if path.is_file():
        result = agent.review_file(str(path))
        print(f"\n✓ Review complete!")
        print(f"  Issues found: {len(result.issues)}")
        print(f"  Score: {result.overall_score}/10")
        print(f"  Confidence: {result.confidence:.1%}")
        print(f"  Report: {args.output_dir}/codereview.md")
    else:
        # Review folder with append mode
        all_results = []
        folder = Path(path)
        py_files = [f for f in folder.rglob("*.py") 
                    if "__pycache__" not in str(f) and "venv" not in str(f)]
        
        for i, py_file in enumerate(py_files):
            result = agent.review_file(str(py_file), append_report=(i > 0))
            all_results.append(result)
        
        # Generate summary
        _generate_final_summary(agent, all_results, args.output_dir)
        
        print(f"\n✓ Reviewed {len(all_results)} files")
        total_issues = sum(len(r.issues) for r in all_results)
        print(f"  Total issues: {total_issues}")
        print(f"  Reports: {args.output_dir}/")
    
    return 0


